# Operadores Matematicos:   +   -   *   /
    # // (division entera)
    # %  (Modulo)
    # ** (Potencia)
# Operadores de Comparacion:   ==   !=   <   >   <=   >=
# Operadores Logicos:   and   or   not
a, b = 10, 3;

print(a + b, a - b, a * b, a / b, a // b, a % b, a ** b)
print(a > b, a == b, a != b)
print(True and False, True or False, not True)