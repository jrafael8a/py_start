# La asignacion de listas se hace entre corchetes []
numeros = [1, 2, 3, 4, 5]

# Se llaman tambien con corchetes, siendo indice 0 el primer valor, 
# indice 1 el segundo, indice 2 el tercero. etc.
# Indice -1 el ultimo valor, indice -2 el penultimo. etc.
print(numeros[0])   # 1
print(numeros[1])   # 2
print(numeros[-1])  # 5
print(numeros[-2])  # 4