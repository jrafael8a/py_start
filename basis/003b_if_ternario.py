# Un operador ternario es la forma compacta de un condicional if-else en una sola línea.
# Se le llma operador ternario porque lleva 3 elementos: 
#     una condición, un valor si es verdadero y otro valor si es falso.
# La sintaxis es la siguiente:
#       <valor si verdadero> if <condicion> else <valor si falso>

# Ejemplo de operador ternario en Python

# Definimos una variable numérica
edad = 20

# Usamos el operador ternario para decidir si la persona es mayor de edad o menor de edad
resultado = "Mayor de edad" if edad >= 18 else "Menor de edad"

# Imprimimos el resultado
print(resultado)  # En este caso, imprimirá "Mayor de edad" porque la edad es 20
