x = 5

if x > 10: # Si x es mayor que 5 se ejecutara este bloque
    print("Mayor que 10")
elif x == 5: # elif significa Else if. Si x es igual a 5 se ejecuta este bloque
    print("Es 5")
else: # Si ninguna de las condicinoes previas se ejecuto, recae en este bloque
    print("Menor que 10 y distinto de 5")