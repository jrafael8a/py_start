# Bucle for
for i in range(5):  # 0, 1, 2, 3, 4
    print(i)


# Bucle for compacto:
for i in range(5): print(i)


# Bucle for comprimido:
items = [f"Item {i}" for i in range(50)]
print(items)

# Bucle While
contador = 0
while contador < 5: # 0, 1, 2, 3, 4
    print(contador)
    contador += 1
     